    private void InsertCmbNPiece(){
        String t[] = {"numPiece"};
        rs = db.querySelect(t ,"g_client_inf");
        try{
            
            while(rs.next()){
                cmb_piece.addItem(rs.getString("numPiece"));
                }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
        
    }
    
    private void InsertCmbModele(){
        String t[] = {"Modele"};
        rs = db.querySelect(t ,"g_client_vehi");
        try{
            
            while(rs.next()){
                cmb_Modele.addItem(rs.getString("Modele"));
                }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
        
    }
    
    private void FilterInfClient(){
        String nomPrenom;
        String t[] = {"nom", "prenom", "date_delivr_piece"};
        rs = db.fcSelectCommand(t ,"g_client_inf", " numPiece ='"+ cmb_piece.getSelectedItem().toString() + "'" );
        try{
            while(rs.next()){
                nomPrenom = rs.getString("nom");
                nomPrenom = rs.getString("prenom") +" "+ nomPrenom;
                txt_NomC.setText(nomPrenom);
                txt_dat_delivr_piece.setText(rs.getString("date_delivr_piece"));
                }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
    }
    
    private void FilterInfVehi(){
        
        String t[] = {"immatricule", "Marque"};
        rs = db.fcSelectCommand(t ,"g_client_vehi", " Modele ='"+ cmb_Modele.getSelectedItem().toString() + "'" );
        try{
            while(rs.next()){
                txt_Marque.setText(rs.getString("Marque"));
                txt_imma.setText(rs.getString("immatricule"));
                }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
    }
    
