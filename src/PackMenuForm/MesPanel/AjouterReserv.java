    private void btnAjoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjoutActionPerformed
        // TODO add your handling code here:
     if(txt_imma.getText().equals("") || txt_D_Reserv.getText().equals("") || txt_F_Reserv.getText().equals("")){

            JOptionPane.showMessageDialog(this, "SVP les champs avec (*) sont obligatoires ");
        }else {
         String b="";
            if(rbtn_espece.isSelected()){
                b ="Espèce";
            }else if(rbtn_cheque.isSelected()){
                b ="Chèque";
            }else if(rbtn_carte.isSelected()){
                b ="Carte credit";
            }
            String[] col = {"numPiece", "immatricule", "Date_depart", "Lieu_depart", "Date_retour", "Lieu_retour", "Montant", "Mode_paiement"};
            String[] inf = {cmb_piece.getSelectedItem().toString(), txt_imma.getText(), txt_D_Reserv.getText(),
                cmb_lieuD.getSelectedItem().toString(), txt_F_Reserv.getText(), cmb_lieuF.getSelectedItem().toString(), txt_Montant.getText(), b};
            try{
                System.out.println(db.queryInsert("g_reservation", col, inf));
                JOptionPane.showMessageDialog(this, "Enregistrer avec succée ");
            }catch(Exception e){
                System.out.println(e.getMessage());
                System.out.println("L ajoute du client n est pas valider");
            }
        }
    }//GEN-LAST:event_btnAjoutActionPerformed

